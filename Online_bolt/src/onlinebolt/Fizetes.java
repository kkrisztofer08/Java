    private void jButtonVeglegesitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVeglegesitActionPerformed
        
        //a vevő adatatinak elmentése egy fájlba
        try{
            PrintWriter writer = new PrintWriter("vasarlo.txt", "UTF-8");
            writer.append(this.jTextFieldName.getText() + ";" + this.jTextFieldAddress.getText()+ ";" + this.jTextFieldTelephone.getText() + ";" + this.jTextFieldBankcard.getText()+ ";" + this.termekekDarab() + " db termék\n");
            writer.close();
        }catch(IOException ex){
            JOptionPane.showMessageDialog(this, ex + "HIBA a fájlba íráskor", "HIBA", JOptionPane.ERROR_MESSAGE);
        }
        
        //a raktáridarabok levonása
        for(Termekek prod : t.termekek)
        {
            for(Termekek kosar : t.kosar)
            {
                if(prod.getTermekNeve().equals(kosar.getTermekNeve()))
                {
                    int raktar = prod.getRaktariDarabszam() - 1;
                    prod.setRaktariDarabaszam(raktar);
                }
            }
        }
        
        //a kosar kiűrítése
        bv.kosarKiurit();
        JOptionPane.showMessageDialog(this, "A fizetés sikeresen megtörtént.\nKöszönjük a vásárlást!", "Értesítés", JOptionPane.INFORMATION_MESSAGE);
        this.setVisible(false);

        Alapfelulet al = new Alapfelulet();
        al.setVisible(true);
    }//GEN-LAST:event_jButtonVeglegesitActionPerformed

