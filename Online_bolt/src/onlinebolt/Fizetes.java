    public int termekekDarab() throws FileNotFoundException, IOException
    {
        BufferedReader reader = new BufferedReader(new FileReader("kosar.txt"));
        int lines = 0;
        while(reader.readLine() != null) lines++;
        reader.close();
        return lines;
    }

